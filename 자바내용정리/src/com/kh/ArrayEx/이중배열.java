package com.kh.ArrayEx;

public class 이중배열 {
/*
	이중배열
	- 배열과 마찬가지로 행과 열로 구성
	- 각 행은 서로 다른 배열을 나타냄
	- 각 열은 해당 행의 요소(데이터)들을 나타냄
	
	- 초기값은 없지만 자리를 나타내는 방식
	자료형[][] 변수명 = new  자료형[행의 크기][열의 크기];
	자료형 변수명[][] = new  자료형[행의 크기][열의 크기];
	
	- 초기값을 처음부터 갖고 있는 이중 배열 선언
	자료형 변수명[][] = {{ }, { }, { }};
	
	ex)
	int arr[][] = new int[2][3];
	
	arr은 행이 0, 1
		 열이 0, 1, 2인 장바구니를 갖게 됨
	*/
}
